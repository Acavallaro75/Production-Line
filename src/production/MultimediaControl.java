package production;

/**
 * The MultimediaControl interface is used to instantiate the play(), stop(), previous(), and next()
 * methods.
 *
 * @author: Andrew Cavallaro
 * @date: 10/29/2019
 */
interface MultimediaControl {

  /** The play() method has no meaningful job at the present moment. */
  void play();

  /** The stop() method has no meaningful job at the present moment. */
  void stop();

  /** The previous() method has no meaningful job at the present moment. */
  void previous();

  /** The next() method has no meaningful job at the present moment. */
  void next();
}
